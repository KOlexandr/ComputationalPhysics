function b_2
clc;
%% ?????????????? ???????????????????? ?????????? ???? ???????????? (???????????????? ???????????????? ??????????)

m = 100;

K = 0:1e-2:1;
Gamma = 0:1e-2:1;

for Init = 1e-2:1e-2:1-1e-2
    
    hold on;
    xlabel('Gamma');
    ylabel('K');
    title('Arnold Tongue');
    for i = 1:length(K)
        for j = 1:length(Gamma)
            Teta(i,j) = Init;
            for k=1:m
                Teta(i,j) = mod(Teta(i,j) + Gamma(j) - K(i)/(2*pi)*sin(2*pi*Teta(i,j)), 1);
            end
        end
    end
    surf(Gamma,K,Teta);
    if Init == 1-1e-2
       savefig('fig3.fig');
    end
    pause(1e-5);
end